#Calculator functions


def add2numbers(x,y):
    return x + y
def subtract2numbers(x,y):
    return x - y
def multiply2numbers(x,y):
    return x * y
def divide2numbers(x,y):
    return x / y
def changesign(x):
    return x * -1
def percent(x):
    return x / 100
def squareroot(x):
    return x ** .5
def square(x):
    return x ** 2
def inverse(x):
    return 1 / x
    
